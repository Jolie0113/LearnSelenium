public class MySwitchStatements {
    public static void main(String[] args) {
        int months =1;
        switch (months){
            case 1:
                System.out.println("Jan"); break;
            case 2:
                System.out.println("Feb"); break;
            case 3:
                System.out.println("Feb"); break;
            //
            case 12:
                System.out.println("Dec"); break;
            default:
                System.out.println("Not a valid value!");

        }

    }
}
